// 5. Lee un número por teclado e indica si es divisible entre 2 (resto = 0). Si no lo es,
// también debemos indicarlo.

 var numero = parseInt(prompt("Escribe un numero"))
 if ((numero % 2) == 0 ){
     console.log(numero + " es divisible por 2")
 } else {
    console.log(numero + " no es divisible por 2")
 }


